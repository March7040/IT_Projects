describe('Критичний шлях: Додавання товару в кошик', () => {
  it('Користувач може успішно додати товар у кошик', () => {
    // 1. Заходимо на сторінку
    cy.visit('http://localhost:3000')
    // 2. Знаходимо кнопку "Купити" та натискаємо
    cy.contains('Add to cart').click();
    // 3. Перевіряємо, чи змінився лічильник у кошику
    cy.get('.cart-count').should('contain', '1');
  });
});